#include <iostream>

using namespace std;

short int a = 0;
int b = 525;
long long c = 1'000'000'000'000;
char d = 'D';
bool e = true;
float f = 3.14f;
double g = 3.14159;

int main()
{
    cout << "Short int variable : " << a << endl;
    cout << "int variable : " << b << endl;
    cout << "long long variable: " << c << endl;
    cout << "char variable: " << d << endl;
    cout << "bool variable: " << e << endl;
    cout << "float variable: " << f << endl;
    cout << "double variable: " << g << endl;

    return 0;
}
